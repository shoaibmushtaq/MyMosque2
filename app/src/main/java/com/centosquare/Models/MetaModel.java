package com.centosquare.Models;

public class MetaModel {

    private int current_page;
    private int from;
    private int last_page;
    private String path;
    private int per_page;
    private int to;
    private int total;


    public int getCurrent_page() {
        return current_page;
    }

    public int getFrom() {
        return from;
    }

    public int getLast_page() {
        return last_page;
    }

    public String getPath() {
        return path;
    }

    public int getPer_page() {
        return per_page;
    }

    public int getTo() {
        return to;
    }

    public int getTotal() {
        return total;
    }
}
